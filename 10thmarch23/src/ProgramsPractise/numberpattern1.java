package ProgramsPractise;

public class numberpattern1 {

	public static void main(String[] args) {

		for (int i = 1; i <= 5; i++) {// for printing the rows
			for (int j = 1; j <= i; j++) {
				System.out.print(j + " ");

			}

			System.out.println();
		}
	

	for (int k=1; k<=5; k++) {// for printing the rows
		
		for (int l=1; l<=k; l++) {
			System.out.print(k+" ");
		}
		System.out.println();
		}
		
		
	}
		
		
		
		
		
	}


