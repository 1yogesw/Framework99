package Loopsdemo;

public class forloopdemo {
	
	public static void main(String[] args) {
		


	
	  // print 1 to 10
	  /*
	   *  syntax for for-loop
	   *  
	   *  for(initialization, condition, increment/decrement){
	   * 
	   *  }
	   */
	  
	  for(int i=0;i<=5;i++) {
	  
	   System.out.println("hello");
	  
	  }
	   

	  
	  
	for(int i=1; i<=5;i++) {
	
		System.out.println(i);
		
	}
	 
	
	 for(int i=5;i<=1;i--) {
		 System.out.println(i);
	 }
	
	 
	for(int i=5;i>=5;i--) {
		System.out.println(i);//
	}
	
	
	
	  
}
}