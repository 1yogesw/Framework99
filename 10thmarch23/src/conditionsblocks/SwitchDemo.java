package conditionsblocks;

public class SwitchDemo {
	
	 /*
	  * switch(expression){    
	 case value1:    
	  //code to be executed;    
	  break;  //optional  
	 case value2:    
	  //code to be executed;    
	  break;  //optional  
	 ......    
	     
	 default:     
	   code to be executed if all cases are not matched;  
	 }    
	  */
	 public static void main(String[] args) {
		 
		 int day=2;
		 
		 switch(day) {
			 
		 case 1:
		System.out.println("monday");
			
		 case 2:
		System.out.println("tuesday");
		 break;
		
		 case 3:
			 System.out.println("wed");
			 break;
			 
		 case 4:
			 System.out.println("thur");
			 
			 default:
				 System.out.println("sunday");
		
		
		 }
		 
		 int day1=1;
		  
		  switch(day1)
				 {
		 
		  case 1:  
		     if(day1==1) {
		    System.out.println("monday");
		    break;
		     }
		    
		     
		  case 2:
		  System.out.println("tuesday");
		  break;
		 
		   
		  case 3:
		  System.out.println("wed");
		  break;
		  
		  case 4:
		  System.out.println("thur");
		     break;
		  
		  default:     
		  System.out.println("sunday"); 
		  break;
		   
		  case 5:
		   System.out.println("thur");
		   break;
		  
		   
	 }
}
}
	 

		 
		 
		 
		 
		 
		 
		 
	

	
	
	
	
	
	
	
	


